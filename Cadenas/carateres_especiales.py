#Programa: Ejemplo de caracteres especiales en Python

#Salto de line \n
mensaje = "Hola\n\nMundo"
print(mensaje)

#Tabulación \t
mensaje2 = "Hola\tMundo"
print(mensaje2)

#Imprimir una guía numerica para ver las posiciónes
print(12341234123412341234123412341234)

#Comillas dentro de cadenas
mensaje3 = "Ella dijo: \"Hola a Todos\""
print(mensaje3)

#Varra invertida \
mensje4 = "C:\\Usuarios\\Ferna\\Documentos"
print(mensje4)

#Cadenas crudas (raw string) r
mensaje5 = r"C:\\Usuarios\\Ferna\\Documentos"
print(mensaje5)